package org.wormsim.frontend.models;

public class WormInfo {

    private String wormName;
    private String wormColor;

    public WormInfo() {}

    public String getWormName() {
        return wormName;
    }

    public void setWormName(String wormName) {
        this.wormName = wormName;
    }

    public String getWormColor() {
        return wormColor;
    }

    public void setWormColor(String wormColor) {
        this.wormColor = wormColor;
    }
}
